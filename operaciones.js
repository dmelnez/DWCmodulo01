
export function suma(a, b) {
    let resultado;
  
    a = parseInt(a);
    b = parseInt(b);
  
    if (a == 0 && b == 0) {
      alert("Error en los valores");
    } else {
      resultado = a + b;
    }
  
    return resultado;
  }
  
